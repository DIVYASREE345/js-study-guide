// Prefix increment
let prefixinc = 1;
console.log(++prefixinc); // Expected output 2
console.log(prefixinc); //Expected output 2

// Postfix increment
let postfixinc = 1;
console.log(postfixinc++); // Expected output 1
console.log(postfixinc); // Expected output 2

// Prefix decrement
let prefixdec = 3;
console.log(--prefixdec); // Expected output 2
console.log(prefixdec); //Expected output 2

// Postfix decrement
let postfixdec = 3;
console.log(postfixdec--); // Expected output 3 
console.log(postfixdec); // Expected output 2